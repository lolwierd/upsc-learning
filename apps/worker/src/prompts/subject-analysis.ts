// Subject to analysis mapping
const SUBJECT_ANALYSIS_MAP: Record<string, string> = {
    polity: POLITY_ANALYSIS,
    economy: ECONOMY_ANALYSIS,
    environment: ENVIRONMENT_ANALYSIS,
    geography: GEOGRAPHY_ANALYSIS,
    history: HISTORY_ANALYSIS,
    science: SCIENCE_ANALYSIS,
    "science and technology": SCIENCE_ANALYSIS,
    "art and culture": HISTORY_ANALYSIS, // Art/Culture uses History analysis as they share themes
    art: HISTORY_ANALYSIS,
    culture: HISTORY_ANALYSIS,
};

/**
 * Get subject-specific UPSC pattern analysis for the prompt
 * @param subject - The subject name (polity, economy, etc.)
 * @returns The analysis content string or empty string if not found
 */
export function getSubjectAnalysis(subject: string): string {
    const normalizedSubject = subject.toLowerCase().trim();
    return SUBJECT_ANALYSIS_MAP[normalizedSubject] || "";
}

/**
 * Get the strategic synthesis section for all subjects
 * @returns The strategic synthesis content
 */
export function getStrategicSynthesis(): string {
    return STRATEGIC_SYNTHESIS;
}
